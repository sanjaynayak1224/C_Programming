//TrueFalse
#include<stdio.h>
int main()
{
    printf("%d \n",4==4);               //equal to operator
    printf("%d \n",4!=4);                  //not equal to operator
    printf("%d \n",4>3);
    printf("%d \n",4>3 && 5>8);       // and operator
    printf("%d \n",4>3 || 5>8);       // or operator
    printf("%d \n", !(4>3));
    return 0;
}